package Domain;

/**
 *
 * @author Unskipp
 */
public class Facilities
{
    private int id;
    private String type;
    public Facilities()
    {
        id=0;
        type=null;
    }

    public int getId()
    {
        return id;
    }

    public void setId(int id)
    {
        this.id = id;
    }

    public String getType()
    {
        return type;
    }

    public void setType(String type)
    {
        this.type = type;
    }
    
    
}
